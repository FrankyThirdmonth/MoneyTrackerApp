package com.frankydev.moneytrackerapp.model;

public class GraphData {

    private float valueX;
    private float valueY;

    public GraphData(float valueX, float valueY) {
        this.valueX = valueX;
        this.valueY = valueY;
    }

    public float getValueX() {
        return valueX;
    }

    public float getValueY() {
        return valueY;
    }
}
